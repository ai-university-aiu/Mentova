% TRANSFORM REGISTRY
% arc_named_rule(+RuleName) — enumerate all known transform names.
% arc_transform(+Rule, +Grid, -OutputGrid) — apply Rule to Grid.
% ---------------------------------------------------------------------------

% --- Geometric transforms ---

% Enumerate reverse_rows as a known rule name.
arc_named_rule(reverse_rows).
% Define reverse_rows: reverse each row left-to-right.
arc_transform(reverse_rows, Grid, Result) :-
    % Apply reverse to every row of the grid.
    maplist([Row, Rev]>>(reverse(Row, Rev)), Grid, Result).

% Enumerate vertical_flip as a known rule name.
arc_named_rule(vertical_flip).
% Define vertical_flip: reverse the order of rows top-to-bottom.
arc_transform(vertical_flip, Grid, Result) :-
    % Reverse the list of rows.
    reverse(Grid, Result).

% Enumerate transpose as a known rule name.
arc_named_rule(transpose).
% Define transpose: swap rows and columns.
arc_transform(transpose, Grid, Result) :-
    % Extract the first row to determine the number of columns.
    Grid = [FirstRow|_],
    % Get the number of columns.
    length(FirstRow, NCols),
    % Build column indices.
    numlist(1, NCols, ColIdxs),
    % For each column index, extract that column from the grid to form a row.
    maplist([CI, Col]>>(maplist([R,E]>>(nth1(CI,R,E)), Grid, Col)), ColIdxs, Result).

% Enumerate rotate_90_cw as a known rule name.
arc_named_rule(rotate_90_cw).
% Define rotate_90_cw: transpose then reverse each row.
arc_transform(rotate_90_cw, Grid, Result) :-
    % Transpose the grid first.
    arc_transform(transpose, Grid, T),
    % Then reverse each row.
    arc_transform(reverse_rows, T, Result).

% Enumerate rotate_90_ccw as a known rule name.
arc_named_rule(rotate_90_ccw).
% Define rotate_90_ccw: reverse each row then transpose.
arc_transform(rotate_90_ccw, Grid, Result) :-
    % Reverse each row first.
    arc_transform(reverse_rows, Grid, T),
    % Then transpose.
    arc_transform(transpose, T, Result).

% Enumerate rotate_180 as a known rule name.
arc_named_rule(rotate_180).
% Define rotate_180: vertical flip then reverse each row.
arc_transform(rotate_180, Grid, Result) :-
    % Flip rows top-to-bottom.
    arc_transform(vertical_flip, Grid, T),
    % Then reverse each row.
    arc_transform(reverse_rows, T, Result).

% Enumerate diagonal_flip as a known rule name.
arc_named_rule(diagonal_flip).
% Define diagonal_flip: transpose then vertical flip (flip across anti-diagonal).
arc_transform(diagonal_flip, Grid, Result) :-
    % Transpose the grid.
    arc_transform(transpose, Grid, T),
    % Then flip vertically.
    arc_transform(vertical_flip, T, Result).

% Enumerate anti_diagonal_flip as a known rule name.
arc_named_rule(anti_diagonal_flip).
% Define anti_diagonal_flip: vertical flip then transpose (other anti-diagonal).
arc_transform(anti_diagonal_flip, Grid, Result) :-
    % Flip vertically.
    arc_transform(vertical_flip, Grid, T),
    % Then transpose.
    arc_transform(transpose, T, Result).

% Enumerate identity as a known rule name.
arc_named_rule(identity).
% Define identity: output = input (some ARC tasks are identity on test).
arc_transform(identity, Grid, Grid).

% --- Gravity transforms ---

% Enumerate gravity_down as a known rule name.
arc_named_rule(gravity_down).
% Define gravity_down: non-zero cells fall to the bottom of each column.
arc_transform(gravity_down, Grid, Result) :-
    % Get grid dimensions.
    length(Grid, NRows),
    Grid = [FirstRow|_],
    length(FirstRow, NCols),
    % Process each column index.
    numlist(1, NCols, ColIdxs),
    maplist([CI,OutCol]>>(
        % Extract column CI from the grid.
        maplist([Row,Cell]>>(nth1(CI,Row,Cell)), Grid, ColCells),
        % Filter to non-zero cells.
        include([C]>>(C \= 0), ColCells, NonZero),
        % Count zeros to prepend.
        length(NonZero, NNZ),
        NZeros is NRows - NNZ,
        % Build result column: zeros then non-zero cells.
        length(Zeros, NZeros),
        maplist(=(0), Zeros),
        append(Zeros, NonZero, OutCol)
    ), ColIdxs, OutCols),
    % Reconstruct rows from columns.
    numlist(1, NRows, RowIdxs),
    maplist([RI,Row]>>(
        maplist([Col,Cell]>>(nth1(RI,Col,Cell)), OutCols, Row)
    ), RowIdxs, Result).

% Enumerate gravity_up as a known rule name.
arc_named_rule(gravity_up).
% Define gravity_up: non-zero cells float to the top of each column.
arc_transform(gravity_up, Grid, Result) :-
    % Get grid dimensions.
    length(Grid, NRows),
    Grid = [FirstRow|_],
    length(FirstRow, NCols),
    % Process each column index.
    numlist(1, NCols, ColIdxs),
    maplist([CI,OutCol]>>(
        % Extract column CI.
        maplist([Row,Cell]>>(nth1(CI,Row,Cell)), Grid, ColCells),
        % Keep non-zero cells.
        include([C]>>(C \= 0), ColCells, NonZero),
        % Compute trailing zeros.
        length(NonZero, NNZ),
        NZeros is NRows - NNZ,
        length(Zeros, NZeros),
        maplist(=(0), Zeros),
        % Non-zero cells go first, then zeros.
        append(NonZero, Zeros, OutCol)
    ), ColIdxs, OutCols),
    % Reconstruct rows.
    numlist(1, NRows, RowIdxs),
    maplist([RI,Row]>>(
        maplist([Col,Cell]>>(nth1(RI,Col,Cell)), OutCols, Row)
    ), RowIdxs, Result).

% Enumerate gravity_left as a known rule name.
arc_named_rule(gravity_left).
% Define gravity_left: non-zero cells slide to the left of each row.
arc_transform(gravity_left, Grid, Result) :-
    % Process each row independently.
    maplist([Row, OutRow]>>(
        % Filter non-zero cells to the front, pad zeros at the end.
        include([C]>>(C \= 0), Row, NonZero),
        length(Row, Len),
        length(NonZero, NNZ),
        NZeros is Len - NNZ,
        length(Zeros, NZeros),
        maplist(=(0), Zeros),
        append(NonZero, Zeros, OutRow)
    ), Grid, Result).

% Enumerate gravity_right as a known rule name.
arc_named_rule(gravity_right).
% Define gravity_right: non-zero cells slide to the right of each row.
arc_transform(gravity_right, Grid, Result) :-
    % Process each row independently.
    maplist([Row, OutRow]>>(
        % Filter non-zero cells, put zeros first.
        include([C]>>(C \= 0), Row, NonZero),
        length(Row, Len),
        length(NonZero, NNZ),
        NZeros is Len - NNZ,
        length(Zeros, NZeros),
        maplist(=(0), Zeros),
        append(Zeros, NonZero, OutRow)
    ), Grid, Result).

% --- Sort transforms ---

% Enumerate sort_rows_asc as a known rule name.
arc_named_rule(sort_rows_asc).
% Define sort_rows_asc: sort rows by ascending sum of cell values.
arc_transform(sort_rows_asc, Grid, Result) :-
    % Tag each row with its sum, sort, then untag.
    maplist([Row, Sum-Row]>>(sumlist(Row, Sum)), Grid, Tagged),
    msort(Tagged, Sorted),
    pairs_values(Sorted, Result).

% Enumerate sort_rows_desc as a known rule name.
arc_named_rule(sort_rows_desc).
% Define sort_rows_desc: sort rows by descending sum of cell values.
arc_transform(sort_rows_desc, Grid, Result) :-
    % Tag each row with negated sum so msort puts largest first.
    maplist([Row, NegSum-Row]>>(sumlist(Row, S), NegSum is -S), Grid, Tagged),
    msort(Tagged, Sorted),
    pairs_values(Sorted, Result).

% --- Scale transforms ---

% Enumerate scale_up(2) as a known rule name.
arc_named_rule(scale_up(2)).
% Define scale_up(2): each cell becomes a 2x2 block.
arc_transform(scale_up(2), Grid, Result) :-
    arc_scale_up_grid(2, Grid, Result).

% Enumerate scale_up(3) as a known rule name.
arc_named_rule(scale_up(3)).
% Define scale_up(3): each cell becomes a 3x3 block.
arc_transform(scale_up(3), Grid, Result) :-
    arc_scale_up_grid(3, Grid, Result).

% Define arc_scale_up_grid/3: expand each cell to an NxN block.
arc_scale_up_grid(N, Grid, Result) :-
    % For each row, expand horizontally by replicating each cell N times.
    maplist([Row, ExpandedRow]>>(
        % Replicate each cell N times within the row.
        maplist([Cell, Copies]>>(
            length(Copies, N),
            maplist(=(Cell), Copies)
        ), Row, CellCopies),
        append(CellCopies, ExpandedRow)
    ), Grid, ExpandedRows),
    % Replicate each expanded row N times vertically.
    maplist([ERow, NRows]>>(
        length(NRows, N),
        maplist(=(ERow), NRows)
    ), ExpandedRows, RowGroups),
    append(RowGroups, Result).

