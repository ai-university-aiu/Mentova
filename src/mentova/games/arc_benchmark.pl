% ---------------------------------------------------------------------------
% INDUCTION ENGINE
% arc_fits_all(+Rule, +TrainingPairs) — true if Rule correctly maps every
%   training input to its expected output.
% arc_induce_rule_400(+TrainingPairs, -Rule) — find the first rule that fits.
% ---------------------------------------------------------------------------

% Define arc_fits_all/2: base case — no pairs left to check.
arc_fits_all(_, []).
% Define arc_fits_all/2: recursive case — rule must map In to Out for each pair.
arc_fits_all(Rule, [pair(In, Out)|Rest]) :-
    % Apply the rule to the input grid.
    arc_transform(Rule, In, Computed),
    % The computed output must exactly match the expected output.
    Computed = Out,
    % Check remaining pairs.
    arc_fits_all(Rule, Rest).

% Define arc_induce_rule_400/2: find the first named rule that fits all pairs.
arc_induce_rule_400(TrainingPairs, Rule) :-
    % Try each named rule in the order they are declared.
    arc_named_rule(Rule),
    % Accept the first rule that covers all training pairs.
    arc_fits_all(Rule, TrainingPairs),
    % Cut to prevent backtracking to other rules once one is found.
    !.

% ---------------------------------------------------------------------------
% BENCHMARK RUNNER
% ---------------------------------------------------------------------------

% Define arc_benchmark_run/3: run all 400 tasks and collect results.
arc_benchmark_run(Score, 400, Results) :-
    % Collect all task IDs, training pairs, test inputs, and test outputs.
    % arc_agi_task/4 is provided by the arc_tasks module, loaded by the demo.
    findall(
        task(TaskId, TrainingPairs, TestIn, TestOut),
        arc_tasks:arc_agi_task(TaskId, TrainingPairs, TestIn, TestOut),
        Tasks
    ),
    % Process each task and collect pass/fail results.
    maplist(arc_attempt_task, Tasks, Results),
    % Count the passed tasks.
    include([result(_, pass(_))]>>true, Results, Passed),
    length(Passed, Score).

% Define arc_attempt_task/2: attempt to solve one task; return pass or fail.
arc_attempt_task(task(TaskId, TrainingPairs, TestIn, TestOut), Result) :-
    % Try to induce a rule from the training pairs.
    (   arc_induce_rule_400(TrainingPairs, Rule),
        % Apply the induced rule to the test input.
        arc_transform(Rule, TestIn, Computed),
        % Verify the computed output matches the expected output.
        Computed = TestOut
    % If induction and verification succeed, record pass with the rule.
    ->  Result = result(TaskId, pass(Rule))
    % If no rule fits or the output is wrong, record fail.
    ;   Result = result(TaskId, fail)
    ).

