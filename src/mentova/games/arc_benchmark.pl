% Helper: swap two specific colors in a grid.
arc_swap_colors(A, B, Grid, Result) :-
    maplist([Row,Out]>>(
        maplist([C,OutC]>>(
            ( C =:= A -> OutC = B
            ; C =:= B -> OutC = A
            ; OutC = C
            )
        ), Row, Out)
    ), Grid, Result).

% Helper: recolor one specific color to another.
arc_recolor(From, To, Grid, Result) :-
    maplist([Row,Out]>>(
        maplist([C,OutC]>>(
            ( C =:= From -> OutC = To ; OutC = C )
        ), Row, Out)
    ), Grid, Result).

% Helper: add N to each nonblack cell (modulo 10), color-cycle.
arc_color_cycle(N, Grid, Result) :-
    maplist([Row,Out]>>(
        maplist([C,OutC]>>(
            ( C =:= 0 -> OutC = 0
            ; OutC is ((C - 1 + N) mod 9) + 1
            )
        ), Row, Out)
    ), Grid, Result).

