% --- Border and interior fill transforms ---

% Helper: arc_fill_border_all(+Color, +Grid, -Result).
arc_fill_border_all(C, Grid, Result) :-
    length(Grid, NRows),
    Grid = [FirstRow|_],
    length(FirstRow, NCols),
    numlist(1, NRows, RowIdxs),
    maplist([RI, OutRow]>>(
        nth1(RI, Grid, InRow),
        numlist(1, NCols, ColIdxs),
        maplist([CI, OutCell]>>(
            nth1(CI, InRow, InCell),
            ( (RI =:= 1 ; RI =:= NRows ; CI =:= 1 ; CI =:= NCols)
            ->  OutCell = C
            ;   OutCell = InCell
            )
        ), ColIdxs, OutRow)
    ), RowIdxs, Result).

