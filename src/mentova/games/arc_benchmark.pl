/*  Mentova — ARC-AGI-1 Full Benchmark Runner  (Acc_71)

    Runs all 400 public ARC-AGI-1 training tasks through Mentova's
    inductive reasoning engine and reports an honest score.

    Methodology: pure induction from examples, no pretraining on the
    ARC-AGI-1 corpus, transformation rule named glass-box for every
    solved task.  Raw score reported as N/400 and as a percentage.

    The ARC-AGI-1 public training set (Chollet 2019) is stored as
    Prolog facts in data/arc_agi_1/arc_tasks.pl, generated by
    tools/arc_agi_to_prolog.py.

    Predicate interface:
        arc_benchmark_run(-Score, -Total, -Results)
            Run the full benchmark.  Results = list of
            result(TaskId, pass(Rule)) | result(TaskId, fail).
        arc_benchmark_print/0
            Run and print a full report to stdout.
*/

