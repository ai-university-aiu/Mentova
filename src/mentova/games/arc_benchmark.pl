% Define arc_benchmark_print/0: run the full benchmark and print a report.
arc_benchmark_print :-
    % Print the benchmark header.
    format("~n=== ARC-AGI-1 Full Benchmark Run (Acc_71) ===~n"),
    format("400 public training tasks. Pure induction. No pretraining. Glass-box rules.~n~n"),
    % Run the benchmark.
    arc_benchmark_run(Score, Total, Results),
    % Print all passing tasks.
    format("--- SOLVED TASKS ---~n"),
    forall(
        member(result(TaskId, pass(Rule)), Results),
        format("  PASS  ~w  rule: ~w~n", [TaskId, Rule])
    ),
    % Print the score.
    Percent is Score * 100 / Total,
    format("~n--- SCORE ---~n"),
    format("  ~w / ~w = ~2f%~n", [Score, Total, Percent]),
    format("~n  Methodology: pure induction from training examples, no pretraining,~n"),
    format("  transformation rule named glass-box for every solved task.~n"),
    format("~n=== Benchmark complete. ===~n").

